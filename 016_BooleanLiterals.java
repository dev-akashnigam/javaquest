class BooleanLiterals 
{
    public static void main(String args[])
    {
        boolean likeGreenColor = false;  //all characters in boolean literal false are in small.
        boolean likeUnitedStates = true; //all characters in boolean literal true are also in small.

        System.out.println("You like GREEN color : "+likeGreenColor);
        System.out.println("You like UNITED STATES OF AMERICA : "+likeUnitedStates);
    }    
}
