class MaxNMinDecimalNumber 
{
    public static void main(String args[])
    {
        double maxDecimalNo = Double.MAX_VALUE;
        double minDecimalNo = -Double.MAX_VALUE-1;

        System.out.println("Max decimal number supported by Java = "+maxDecimalNo);
        System.out.println("Min decimal number supported by Java = "+minDecimalNo);
    }    
}
